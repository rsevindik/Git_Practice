package day03_Sequences_Variable;

public class WeightConverter {

    public static void main(String[] args) {

        double kg = 92;      // ratio between kg and pound is; 1 kg = 2.2 lbs
        double lb = kg * 2.2;

        System.out.println(lb);

        double lt = 8;
        double gal = lt * 0.27;

        System.out.println(gal);



    }
}
