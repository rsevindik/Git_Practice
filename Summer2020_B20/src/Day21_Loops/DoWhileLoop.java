package Day21_Loops;

public class DoWhileLoop {
    public static void main(String[] args) {


        boolean result = false; // in this case (False) do-while and while loops are going to be the same.!

        while (result) {
            System.out.println("Hello World");

            System.out.println("================");

            do {  // just do it.!
                System.out.println("Hello World");

            } while (result);
        }
    }
}
