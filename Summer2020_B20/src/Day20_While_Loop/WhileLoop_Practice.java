package Day20_While_Loop;

public class WhileLoop_Practice {
    public static void main(String[] args) {

        for (int i = 1; i <=10 ; i++) {
            System.out.print(i+" ");
        }
        System.out.println();

        int num = 1;
        while(num<=10){
            System.out.print(num+" ");

            num++;
        }
    }
}
