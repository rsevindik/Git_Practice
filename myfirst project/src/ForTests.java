public class ForTests {
    public static void main(String[] args) {

        for (int number = 2; number <=100 ; number*=2) {
            System.out.print(number+" ");
        }
        System.out.println();
        for(int x = 10; x >=0; x--) {
            System.out.print(x+" ");
        }
    }
}
