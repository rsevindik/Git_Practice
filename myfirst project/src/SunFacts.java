public class SunFacts {

    public static void main(String[] args) {
        double distance, mass;

        distance = 1.495979E11; // 1.495979 * 10 ^11(10 power of 11) scientific notation
        mass = 1.989e30; // 1.989 * 10^30 (10 power 30) SN which can be written in lower or capital letter

        System.out.println("The sun is "+ distance +" meters away.");
        System.out.println("The sun's mass is "+ mass +" kilograms.");

    }

}
