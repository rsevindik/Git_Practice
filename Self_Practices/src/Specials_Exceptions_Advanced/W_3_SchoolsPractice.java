package Specials_Exceptions_Advanced;

public class W_3_SchoolsPractice{
    public static void main(String[] args) {

        int x = 10, y = 20;
        System.out.println(Math.max(x,y)); // give you the max num.!

        System.out.println(Math.sqrt(x));  // square root.!

        System.out.println(Math.random()); // generates a random double number between 0 -1 .!

        double a = Math.random()*14;       // technically you get a random number between 0-14 this time
        System.out.println(a);






    }
}
